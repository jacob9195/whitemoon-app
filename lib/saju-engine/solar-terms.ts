/**
 * lib/saju-engine/solar-terms.ts
 *
 * 절기(節氣) 정확한 절입 시각 데이터
 *
 * 사주에서 월주(月柱)를 결정하는 12절:
 * 입춘, 경칩, 청명, 입하, 망종, 소서, 입추, 백로, 한로, 입동, 대설, 소한
 *
 * 연주(年柱)도 입춘을 기준으로 바뀜.
 *
 * 데이터 출처: 한국천문연구원 절기 데이터 기반 천문 계산
 * (태양 황경 기반 정밀 계산값, 분 단위까지 정확)
 *
 * 형식: [year, month(0-based), day, hour, minute, second]
 * 시각은 한국 표준시(KST = UTC+9) 기준
 *
 * solarTermIndex: 12절 중 순서
 * 1=입춘, 2=경칩, 3=청명, 4=입하, 5=망종, 6=소서
 * 7=입추, 8=백로, 9=한로, 10=입동, 11=대설, 12=소한
 */

export interface SolarTermEntry {
  year: number
  termIndex: number  // 1~12 (1=입춘, 12=소한)
  name: string
  // KST 기준 절입 시각
  month: number      // 1-based
  day: number
  hour: number
  minute: number
  second: number
}

