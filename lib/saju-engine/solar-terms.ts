// 유틸리티: 특정 연도의 절기 절입 시각을 UTC 밀리초로 반환
function toKSTMs(year: number, month: number, day: number, hour: number, minute = 0): number {
  // KST = UTC+9, 따라서 UTC = KST - 9시간
  return Date.UTC(year, month - 1, day, hour - 9, minute, 0)
}

// 절기 유형
export type SolarTermType =
  | 'ipchun'      // 입춘 (사주 1월 시작, 연주 교체)
  | 'gyeongchip'  // 경칩 (사주 2월)
  | 'cheongmyeong' // 청명 (사주 3월)
  | 'ipha'        // 입하 (사주 4월)
  | 'mangjong'    // 망종 (사주 5월)
  | 'soser'       // 소서 (사주 6월)
  | 'ipchu'       // 입추 (사주 7월)
  | 'baengno'     // 백로 (사주 8월)
  | 'hanlo'       // 한로 (사주 9월)
  | 'ipdong'      // 입동 (사주 10월)
  | 'daesol'      // 대설 (사주 11월)
  | 'sohan'       // 소한 (사주 12월)

export interface SolarTermInfo {
  type: SolarTermType
  name: string
  termIndex: number      // 1~12
  yearIdx: number        // 몇 번째 데이터
  timestampMs: number    // UTC 밀리초
}

// 연도별 절기 데이터를 빌드
function buildTermMap(): Map<number, SolarTermInfo[]> {
  const map = new Map<number, SolarTermInfo[]>()

  const datasets: [SolarTermType, string, number, [number, number, number, number][] | [number, number, number, number, number, number][]][] = [
    ['ipchun',       '입춘(立春)', 1, IPCHUN_DATA as any],
    ['gyeongchip',   '경칩(驚蟄)', 2, GYEONGCHIP_DATA],
    ['cheongmyeong', '청명(淸明)', 3, CHEONGMYEONG_DATA],
    ['ipha',         '입하(立夏)', 4, IPHA_DATA],
    ['mangjong',     '망종(芒種)', 5, MANGJONG_DATA],
    ['soser',        '소서(小暑)', 6, SOSER_DATA],
    ['ipchu',        '입추(立秋)', 7, IPCHU_DATA],
    ['baengno',      '백로(白露)', 8, BAENGNO_DATA],
    ['hanlo',        '한로(寒露)', 9, HANLO_DATA],
    ['ipdong',       '입동(立冬)', 10, IPDONG_DATA],
    ['daesol',       '대설(大雪)', 11, DAESOL_DATA],
    ['sohan',        '소한(小寒)', 12, SOHAN_DATA],
  ]

  for (const [type, name, termIndex, data] of datasets) {
    for (const row of data) {
      const [year, month, day, hour, minute = 0] = row
      const ms = toKSTMs(year, month, day, hour, minute)
      if (!map.has(year)) map.set(year, [])
      map.get(year)!.push({ type, name, termIndex, yearIdx: year, timestampMs: ms })
    }
  }

  return map
}

let _termMap: Map<number, SolarTermInfo[]> | null = null

function getTermMap(): Map<number, SolarTermInfo[]> {
  if (!_termMap) _termMap = buildTermMap()
  return _termMap
}

/**
 * 특정 연도의 특정 절기 절입 시각(UTC ms)을 반환
 * 데이터 없으면 null
 */
export function getSolarTermMs(year: number, termType: SolarTermType): number | null {
  const termMap = getTermMap()
  const terms = termMap.get(year)
  if (!terms) return null
  const found = terms.find(t => t.type === termType)
  return found ? found.timestampMs : null
}

/**
 * 주어진 UTC ms 시각이 속하는 사주 월(月支)를 결정
 *
 * 사주 월주는 절기 기준으로 바뀜:
 * - 입춘(1월寅)→경칩(2월卯)→청명(3월辰)→입하(4월巳)→망종(5월午)→소서(6월未)
 * - 입추(7월申)→백로(8월酉)→한로(9월戌)→입동(10월亥)→대설(11월子)→소한(12월丑)
 *
 * @returns { monthIndex: 1~12, branchIndex: 0~11 }
 */
export function getSajuMonth(timestampMs: number): {
  monthIndex: number     // 사주 월 1=인월(寅) ~ 12=축월(丑)
  branchIdx: number      // 지지 인덱스 0=子 ~ 11=亥
  termName: string
  termYear: number
} {
  // 해당 연도와 인접 연도 탐색 (소한은 다음 해 1월에 있음)
  const date = new Date(timestampMs)
  const year = date.getUTCFullYear()
  const termMap = getTermMap()

  // 탐색 순서: 12절기를 시간 역순으로 순회해서 timestampMs 이후인 절기 이전 절기를 찾음
  // 소한(12월)은 다음 해 1월에 위치
  const termTypes: [SolarTermType, number, number][] = [
    // [type, monthIndex, branchIdx]
    ['ipchun',       1,  2],  // 寅
    ['gyeongchip',   2,  3],  // 卯
    ['cheongmyeong', 3,  4],  // 辰
    ['ipha',         4,  5],  // 巳
    ['mangjong',     5,  6],  // 午
    ['soser',        6,  7],  // 未
    ['ipchu',        7,  8],  // 申
    ['baengno',      8,  9],  // 酉
    ['hanlo',        9,  10], // 戌
    ['ipdong',       10, 11], // 亥
    ['daesol',       11, 0],  // 子
    ['sohan',        12, 1],  // 丑
  ]

  // 검색 범위: 작년 소한 ~ 올해 소한
  const searchYears = [year - 1, year, year + 1]
  const candidates: { ms: number; monthIndex: number; branchIdx: number; termName: string; termYear: number }[] = []

  for (const sy of searchYears) {
    const terms = termMap.get(sy)
    if (!terms) continue
    for (const [type, monthIndex, branchIdx] of termTypes) {
      const found = terms.find(t => t.type === type)
      if (found) {
        candidates.push({
          ms: found.timestampMs,
          monthIndex,
          branchIdx,
          termName: found.name,
          termYear: sy,
        })
      }
    }
  }

  // 시간 순 정렬
  candidates.sort((a, b) => a.ms - b.ms)

  // timestampMs보다 작거나 같은 절기 중 가장 최근 것
  let result = candidates[0] // 기본값
  for (const c of candidates) {
    if (c.ms <= timestampMs) {
      result = c
    } else {
      break
    }
  }

  return result
}

/**
 * 특정 UTC ms 시각의 사주 연도(年支, 입춘 기준)를 반환
 */
export function getSajuYear(timestampMs: number): {
  year: number       // 실제 연도
  sajuYear: number   // 사주 연도 (입춘 기준)
  ipchunMs: number   // 해당 사주 연도 입춘 절입 시각
} {
  const date = new Date(timestampMs)
  const year = date.getUTCFullYear()

  // 이 해의 입춘 시각
  const thisIpchun = getSolarTermMs(year, 'ipchun')
  const prevIpchun = getSolarTermMs(year - 1, 'ipchun')

  if (thisIpchun && timestampMs >= thisIpchun) {
    return { year, sajuYear: year, ipchunMs: thisIpchun }
  } else if (prevIpchun) {
    return { year, sajuYear: year - 1, ipchunMs: prevIpchun }
  }

  // 데이터 없는 경우 양력 연도 사용
  return { year, sajuYear: year, ipchunMs: timestampMs }
}
